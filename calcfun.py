def add(a,b):
   
    c=a+b
    print(a,"+",b,"=",c)
    
def sub(c,d):
   
    e=c-d
    print(c,"-",d,"=", e)
    
def multi(f,g):
   
    h=f*g
    print(f,"*",g,"=",h)
    
def div(i,j):
    
    if j!=0: 
        k=i/j
        print(i,"/",j,"=",k)
    else:
        print("ZeroDivisionError")
        
def pow(l,m):
    
    n=l**m
    print(l,"**",m,"=",n)


